const ctzsi2 = @import("ctzsi2.zig");
const testing = @import("std").testing;

fn test__ctzsi2(a: u32, expected: i32) void {
    // XXX At high optimization levels this test may be horribly miscompiled if
    // one of the naked implementations is selected.
    var nakedCtzsi2 = ctzsi2.__ctzsi2;
    var actualCtzsi2 = @ptrCast(fn (a: i32) callconv(.C) i32, nakedCtzsi2);
    var x = @bitCast(i32, a);
    var result = actualCtzsi2(x);
    testing.expectEqual(expected, result);
}

